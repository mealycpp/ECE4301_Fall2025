static const uint8_t Rcon[11] = {
  0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36
};

static void KeyExpansion(uint8_t* RoundKey, const uint8_t* Key) {
  unsigned i, k;
  uint8_t tempa[4];
  for (i = 0; i < AES_KEYLEN; ++i) RoundKey[i] = Key[i];
  for (i = AES_KEYLEN; i < AES_keyExpSize; i += 4) {
    for (k = 0; k < 4; ++k) tempa[k] = RoundKey[(i - 4) + k];
    if (i % AES_KEYLEN == 0) {
      uint8_t t = tempa[0]; tempa[0]=tempa[1]; tempa[1]=tempa[2]; tempa[2]=tempa[3]; tempa[3]=t;
      tempa[0] = sbox[tempa[0]]; tempa[1] = sbox[tempa[1]]; tempa[2] = sbox[tempa[2]]; tempa[3] = sbox[tempa[3]];
      tempa[0] ^= Rcon[i / AES_KEYLEN];
    }
    RoundKey[i + 0] = RoundKey[i - AES_KEYLEN + 0] ^ tempa[0];
    RoundKey[i + 1] = RoundKey[i - AES_KEYLEN + 1] ^ tempa[1];
    RoundKey[i + 2] = RoundKey[i - AES_KEYLEN + 2] ^ tempa[2];
    RoundKey[i + 3] = RoundKey[i - AES_KEYLEN + 3] ^ tempa[3];
  }
}

static uint8_t xtime(uint8_t x) { return (uint8_t)((x<<1) ^ (((x>>7) & 1) * 0x1b)); }

static void SubBytes(uint8_t* s) { for (int i=0;i<16;++i) s[i]=sbox[s[i]]; }
static void InvSubBytes(uint8_t* s){ for (int i=0;i<16;++i) s[i]=rsbox[s[i]]; }

static void ShiftRows(uint8_t* s) {
  uint8_t t;
  t = s[1]; s[1]=s[5]; s[5]=s[9]; s[9]=s[13]; s[13]=t;
  t = s[2]; s[2]=s[10]; s[10]=t; t=s[6]; s[6]=s[14]; s[14]=t;
  t = s[3]; s[3]=s[15]; s[15]=s[11]; s[11]=s[7]; s[7]=t;
}
static void InvShiftRows(uint8_t* s) {
  uint8_t t;
  t=s[13]; s[13]=s[9]; s[9]=s[5]; s[5]=s[1]; s[1]=t;
  t=s[2]; s[2]=s[10]; s[10]=t; t=s[6]; s[6]=s[14]; s[14]=t;
  t=s[3]; s[3]=s[7]; s[7]=s[11]; s[11]=s[15]; s[15]=t;
}

static void MixColumns(uint8_t* s) {
  for (int i=0;i<4;++i) {
    int idx=i*4;
    uint8_t a=s[idx+0], b=s[idx+1], c=s[idx+2], d=s[idx+3];
    uint8_t e=a^b^c^d;
    uint8_t xa=xtime(a^b); s[idx+0]^=xa^e;
    uint8_t xb=xtime(b^c); s[idx+1]^=xb^e;
    uint8_t xc=xtime(c^d); s[idx+2]^=xc^e;
    uint8_t xd=xtime(d^a); s[idx+3]^=xd^e;
  }
}

static uint8_t xtime_mul(uint8_t a, uint8_t b){
  uint8_t res=0;
  while(b){
    if(b&1) res^=a;
    uint8_t hi=a&0x80;
    a<<=1;
    if(hi) a^=0x1b;
    b>>=1;
  }
  return res;
}

static void InvMixColumns(uint8_t* s){
  for(int i=0;i<4;i++){
    int idx=i*4;
    uint8_t a=s[idx+0], b=s[idx+1], c=s[idx+2], d=s[idx+3];
    s[idx+0]=xtime_mul(a,0x0e)^xtime_mul(b,0x0b)^xtime_mul(c,0x0d)^xtime_mul(d,0x09);
    s[idx+1]=xtime_mul(a,0x09)^xtime_mul(b,0x0e)^xtime_mul(c,0x0b)^xtime_mul(d,0x0d);
    s[idx+2]=xtime_mul(a,0x0d)^xtime_mul(b,0x09)^xtime_mul(c,0x0e)^xtime_mul(d,0x0b);
    s[idx+3]=xtime_mul(a,0x0b)^xtime_mul(b,0x0d)^xtime_mul(c,0x09)^xtime_mul(d,0x0e);
  }
}

static void AddRoundKey(uint8_t round, uint8_t* state, const uint8_t* RoundKey) {
  for (int i = 0; i < 16; ++i) state[i] ^= RoundKey[round * AES_BLOCKLEN + i];
}

void AES_init_ctx(AES_ctx* ctx, const uint8_t* key) { KeyExpansion(ctx->RoundKey, key); }
void AES_init_ctx_iv(AES_ctx* ctx, const uint8_t* key, const uint8_t* iv) { AES_init_ctx(ctx,key); memcpy(ctx->Iv, iv, 16); }
void AES_ctx_set_iv(AES_ctx* ctx, const uint8_t* iv) { memcpy(ctx->Iv, iv, 16); }

static void Cipher(uint8_t* state, const uint8_t* RoundKey) {
  AddRoundKey(0, state, RoundKey);
  for (uint8_t round = 1; round < 10; ++round) {
    SubBytes(state); ShiftRows(state); MixColumns(state); AddRoundKey(round, state, RoundKey);
  }
  SubBytes(state); ShiftRows(state); AddRoundKey(10, state, RoundKey);
}

static void InvCipher(uint8_t* state, const uint8_t* RoundKey){
  AddRoundKey(10, state, RoundKey);
  for(int round=9; round>0; --round){
    InvShiftRows(state); InvSubBytes(state); AddRoundKey(round, state, RoundKey); InvMixColumns(state);
  }
  InvShiftRows(state); InvSubBytes(state); AddRoundKey(0, state, RoundKey);
}

void AES_ECB_encrypt(const AES_ctx* ctx, uint8_t* buf){ Cipher(buf, ctx->RoundKey); }
void AES_ECB_decrypt(const AES_ctx* ctx, uint8_t* buf){ InvCipher(buf, ctx->RoundKey); }

void AES_CBC_encrypt_buffer(AES_ctx* ctx, uint8_t* buf, size_t length) {
  uint8_t* Iv = ctx->Iv;
  for (size_t i = 0; i < length; i += 16) {
    for (int j = 0; j < 16; ++j) buf[i+j] ^= Iv[j];
    uint8_t block[16];
    memcpy(block, buf + i, 16);
    Cipher(block, ctx->RoundKey);
    memcpy(buf + i, block, 16);
    memcpy(Iv, block, 16);
  }
}

void AES_CBC_decrypt_buffer(AES_ctx* ctx, uint8_t* buf, size_t length) {
  uint8_t prev_ct[16], tmp[16];
  uint8_t Iv_local[16]; memcpy(Iv_local, ctx->Iv, 16);
  for (size_t i=0; i<length; i+=16){
    memcpy(tmp, buf+i, 16);              // current ciphertext
    uint8_t block[16]; memcpy(block, buf+i, 16);
    InvCipher(block, ctx->RoundKey);     // AES-ECB decrypt
    for(int j=0;j<16;++j) block[j] ^= Iv_local[j]; // CBC XOR
    memcpy(buf+i, block, 16);
    memcpy(Iv_local, tmp, 16);           // next IV = current ciphertext
  }
  memcpy(ctx->Iv, Iv_local, 16);
}
